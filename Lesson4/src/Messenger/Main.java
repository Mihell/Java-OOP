package Messenger;

public class Main {
    public static void main(String[] args) {
        Message hello = new Message("Me", new PlainText("Hello"));
        Message smile = new Message("Alex", new EmoticonText("winking"));
        Message how = new Message("Me", new PlainText("How are you?"));
        Message ok = new Message("Alex", new PictureText("like"));
        Dialog dialog = new Dialog();
        dialog.addMessage(hello)
                .addMessage(smile)
                .addMessage(how)
                .addMessage(ok)
                .addMessage(new Message ("Me", new PlainText("I see you love ASCII art and smileys")))
                .addMessage(new Message("Alex", new PictureText("heart")))
                .addMessage(new Message ("Me", new PlainText("Whats wrong with you!!?")))
                .addMessage(new Message("Alex", new EmoticonText("confused")))
                .addMessage(new Message("Alex", new EmoticonText("angry")))
                .addMessage(new Message("Me", new EmoticonText("joy")));
        dialog.printAllMessages();
    }
}
